<h1>Meatballs</h1>
<img src="images/Meatballs.jpg">
<h2>Ingredients</h2>
<ul>
    <li>2 free-range eggs</li>
    <li>125ml/4fl oz milk</li>
    <li>450g/1lb minced venison steak</li>
    <li>200g/7oz breadcrumbs</li>
    <li>75g/3oz parmesan, grated</li>
    <li>1 garlic clove, finely chopped</li>
    <li>1 tbsp vegetable oil</li>
</ul>
<h2>Preparation</h2>
<ul>
    <li>Beat the eggs and milk together in a bowl. Add the minced venison, breadcrumbs, parmesan and garlic and mix
        well.
    </li>
    <li>Mould the meatball mixture into balls, each the size of a golf ball.</li>
    <li>Place the meatballs onto a plate or tray and chill in the fridge for 20 minutes.</li>
    <li>Heat the oil in a frying pan over a medium heat.</li>
    <li>Remove the meatballs from the fridge and place into a hot frying pan. Fry for 8-10 minutes, or until
        golden-brown all over.
    </li>
    <li>Add the meatballs to some sauce and simmer for 5-6 minutes, until the meatballs are completely cooked through.
    </li>
    <li>To serve, place a generous portion of the meatballs and sauce into a bowl, and finish with grated parmesan.</li>
</ul>

